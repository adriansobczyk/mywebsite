$(function() {
 
    var progressed = 5;
   
    var interval = setInterval(function() {
   
        progressed += 1;
   
        $("#moving-progress-bar")
   
        .css("width", progressed + "%")
   
        .attr("aria-valuenow", progressed)
   
        .text(progressed + "%");
   
        if (progressed >= 75)
   
            clearInterval(interval);
   
    }, 25);
   
  });

